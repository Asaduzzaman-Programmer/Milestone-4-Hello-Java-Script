const nam = ['abul', 'babul', 'cabul', 'babul' , 'mokbul', 'cabul']
function noDup(arr){
    let uniqe = [];
    for(let one of arr){
        if(uniqe.includes(one)=== false){
            uniqe.push(one)
        }
    }
    return uniqe
}
const result = noDup(nam)
